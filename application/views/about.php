<h2>Standard Deviation Explained</h2>
<p>
    Standard deviation (SD), symbolized by the Greek letter sigma (σ), is a measure used in statistics and probability theory to determine the dispersion of data from its average value. When the standard deviation is low, data points are closely clustered around the mean, whereas a high standard deviation indicates that data points are more dispersed across a broader range.
</p>

<h2>Understanding Standard Deviation</h2>
<p>
